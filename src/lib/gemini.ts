function turnToContent(turn: Turn): Content {
  const parts: Part[] = [];
  if (turn.imageBase64) {
    parts.push({
      inlineData: {
        mimeType: turn.imageMimeType ?? "image/png",
        data: turn.imageBase64,
      },
    });
  }
  if (turn.text) parts.push({ text: turn.text });
  return { role: turn.role, parts };
}

function extractImage(
  response: Awaited<ReturnType<typeof ai.models.generateContent>>,
): EditResult {
  const parts = response.candidates?.[0]?.content?.parts ?? [];
  let imageBase64: string | undefined;
  let mimeType = "image/png";
  let text: string | undefined;

  for (const part of parts) {
    if (part.inlineData?.data) {
      imageBase64 = part.inlineData.data;
      if (part.inlineData.mimeType) mimeType = part.inlineData.mimeType;
    } else if (part.text) {
      text = (text ? text + "\n" : "") + part.text;
    }
  }

  if (!imageBase64) {
    throw new Error(
      text
        ? `Gemini returned no image. Message: ${text}`
        : "Gemini returned no image (likely blocked by safety filters).",
    );
  }

  return { imageBase64, mimeType, text };
}

const PRODUCT_ANALYSIS_PROMPT = [
  "Look at the attached product photo(s) and output a SHORT factual list of",
  "the product's hard-to-render details that downstream image generation",
  "must preserve. Keep the output under 120 words total. Focus only on:",
  "",
  "1. PRINTED TEXT — every word, slogan, label visible on the product,",
  "   transcribed VERBATIM with exact spelling and capitalization, plus its",
  "   position (e.g. 'MY FRIEND' centered on chest, 'EAU DE PARFUM' on bottle",
  "   label).",
  "2. PRINTED GRAPHICS — every illustration, character, logo, or pattern.",
  "   One brief sentence each: subject + colors + position.",
  "3. EMBROIDERED / 3D / APPLIQUE elements — patches, raised motifs, fluffy",
  "   decorations. One brief line each.",
  "4. ANY DISTINCTIVE DETAIL the image generator might miss or fabricate",
  "   wrong (e.g. a specific stitch pattern, a unique closure, a small",
  "   side label).",
  "",
  "Skip generic info that's already obvious from the photo (product type,",
  "color, material, hardware count). Be brief, factual, and focused only",
  "on details that need to be preserved character-for-character.",
].join("\n");

/** Keep the analysis text small so the downstream image-gen request fits
 * comfortably under Vertex's payload threshold. */
const MAX_ANALYSIS_CHARS = 1200;

/**
 * Vision pre-pass: extract a precise text description of the product from
 * the uploaded reference photo(s). The output is fed into the image-gen
 * call as a second source of truth alongside the photos themselves, which
 * helps the image model preserve specific prints, graphics, and hardware
 * that it might otherwise drop or reinterpret.
 *
 * Uses a cheap text+vision model (~$0.0001 / call), so the pre-pass adds
 * negligible cost.
 */
export async function describeProduct(images: InlineImage[]): Promise<string> {
  if (images.length === 0) return "";

  const parts: Part[] = images.map((img) => ({
    inlineData: { mimeType: img.mimeType, data: img.base64 },
  }));
  parts.push({ text: PRODUCT_ANALYSIS_PROMPT });

  const response = await ai.models.generateContent({
    model: VISION_MODEL,
    contents: [{ role: "user", parts }],
  });

  const respParts = response.candidates?.[0]?.content?.parts ?? [];
  let text = "";
  for (const p of respParts) {
    if (p.text) text += p.text;
  }
  text = text.trim();
  if (text.length > MAX_ANALYSIS_CHARS) {
    text = text.slice(0, MAX_ANALYSIS_CHARS) + "…";
  }
  return text;
}

export async function editImage(turns: Turn[]): Promise<EditResult> {
  const contents = turns.map(turnToContent);

  const response = await ai.models.generateContent({
    model: IMAGE_MODEL,
    contents,
  });

  return extractImage(response);
}

