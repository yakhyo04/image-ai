export async function generateInfographic({
  images,
  description,
  aspectRatio = "3:4",
  style = "glass",
  language = "en",
}: InfographicInput): Promise<EditResult> {
  if (images.length === 0) {
    throw new Error("At least one product image is required.");
  }
  if (images.length > 4) {
    throw new Error("Up to 4 product images are supported.");
  }

  // Vision pre-pass DISABLED. It was causing intermittent Vertex 404 errors
  // ("Requested entity was not found.") on the downstream image-generation
  // call — the combined request shape with the analysis text was triggering
  // Vertex Express Mode's image-gen endpoint to fail after ~45s of internal
  // processing. The describeProduct() function below remains available; to
  // re-enable, restore the try/catch call here.
  const productAnalysis = "";

  // Pass 2: image generation. Use ONE combined text part — Vertex's image
  // generation endpoint can fail unpredictably when given multiple text
  // parts in a single request. Recency-biased ordering is preserved inside
  // the single text: styling prompt first, product analysis last.
  const parts: Part[] = images.map((img) => ({
    inlineData: { mimeType: img.mimeType, data: img.base64 },
  }));

  const stylingPrompt = buildInfographicPrompt(description, style, language);
  const combinedText = productAnalysis
    ? [
        stylingPrompt,
        "",
        "AUTHORITATIVE PRODUCT GROUND TRUTH — read this LAST, apply it ABOVE",
        "every other instruction. This was extracted by a vision pre-pass",
        "directly from the reference photo(s). It is the WORD-LEVEL truth of",
        "what the product actually looks like. If anything in the styling",
        "prompt above conflicts with what's listed here, the list below WINS.",
        "If your generated product differs in any printed word, graphic,",
        "embroidered element, or distinctive feature listed here, the output",
        "has failed and must be regenerated.",
        "",
        '"""',
        productAnalysis,
        '"""',
        "",
        "Final instruction: render the product so every item in this ground",
        "truth list is present and unchanged in the output, then apply the",
        "styling.",
      ].join("\n")
    : stylingPrompt;
  parts.push({ text: combinedText });

  const response = await ai.models.generateContent({
    model: INFOGRAPHIC_MODEL,
    contents: [{ role: "user", parts }],
    config: {
      responseModalities: ["IMAGE"],
      imageConfig: { aspectRatio, imageSize: "2K" },
    },
  });

  return extractImage(response);
}
